package frc.robot.subsystems.utils.Position_Enums;

/**
 * Enum representing various positions for the IntakeShooter subsystem.
 * These positions include:
 * - STOW: The default stowed position.
 * - INTAKE: Position for intaking balls.
 * - SHOOT_NEAR_SPEAKER: Position for shooting balls near the speaker.
 * - SHOOT_FAR_SPEAKER: Position for shooting balls far from the speaker.
 * - HANDOFF: Position for handing off balls.
 * - MANUAL: Manual control mode, not a preset position.
 */
public enum IntakeShooterPositions {
    STOW,
    INTAKE,
    SHOOT_NEAR_SPEAKER,
    SHOOT_FAR_SPEAKER,
    HANDOFF,
    MANUAL
}
