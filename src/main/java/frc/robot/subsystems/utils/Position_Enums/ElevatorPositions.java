package frc.robot.subsystems.utils.Position_Enums;

/**
 * Enum representing various positions for the elevator subsystem.
 * These positions include:
 * - PODIUM: Position for interacting with the podium.
 * - AMP: Position for handling the amplifier.
 * - STOW: Default stowed position.
 * - HANDOFF: Position for handing off items.
 * - SOURCE: Position for sourcing items.
 * - MANUAL: Manual control mode, not a preset position.
 */
public enum ElevatorPositions {
    PODIUM,
    AMP,
    STOW,
    HANDOFF, 
    SOURCE,
    MANUAL,
}
